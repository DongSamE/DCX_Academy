package Repeat;

public class Ex03_For {
	public static void main(String[] args) {
		
		//	for repeater -> Clearly n time
		//
		// 	for( Init ; test case ; Operation){
		//		
		//	}
		
		for(int i = 1 ; i < 11 ; i++) {
			System.out.print(i+ " ");
		}
		
		System.out.println(" ");
		
		for( int i = 96 ; i >= 73 ; i--) {
			System.out.print(i+ " ");
		}
		
		System.out.println(" ");
		
		for( int i = 96 ; i >= 73 ; i--) {
			if(i%2==1) {
			System.out.print(i+ " ");
			}
		}
		
	
		
		
	}
}
