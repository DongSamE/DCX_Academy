package Operator;

public class Ex01_Arithmetic {
	public static void main(String[] args) {
		//Arithmetic Operators
		// +, *, -, /(quotient), %(remain)
		
		int num1 = 2;
		int num2 = 5;
		float num3 = 3.14f;
		String num4 = "7";
		
		System.out.println(num1+num2);//7
		System.out.println(num2/num1);//2
		System.out.println(num1+num4);//"27"
		
		//String + Number -> String
		
	}
}
